// Write To Program To Print Counting From 1 to 10
// and 10 to 1 using while loop , do while and for loop

package com.saeedisoft;

public class Exercise1
{
    public static void main(String args[])
    {
        for (int counter = 10; counter >= 1; counter = counter - 1)
        {
            System.out.println(counter);
        }
    }
}
